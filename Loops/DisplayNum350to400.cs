using System;
using System.Collections.Generic;
using System.Text;

namespace Loops
{
    class DisplayNum350to400
    {
        static void Main(String[] args)
        {
            Console.WriteLine("Display number from 350 to 400 ");

            for (int i = 350;i<=400;i++)
            {

                Console.WriteLine(i);


            }
        }
    }
}
