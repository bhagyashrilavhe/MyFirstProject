using System;
using System.Collections.Generic;
using System.Text;

namespace Loops
{
    class Display25to15
    {
        static void Main(String[] args)
        {
            Console.WriteLine("Display number from 25 to 15");

            for (int i = 25; i>=15; i--)
            {

                Console.WriteLine(i);


            }
        }
    }
}
